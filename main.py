class MainGUI:
    def __init__(self):
        self.root = Tk()
        self.root.config(bg="black")
        self.root.geometry("350x150")
        self.root.tk.call('wm', 'iconphoto', self.root._w, PhotoImage(file="icons8-octahedron-64.png"))
        self.root.title("NamGEN")
        self.name_value = ""

        self.menubar = Menu(self.root, bg='black', fg='white')
        self.editmenu = Menu(self.menubar, tearoff=0)
        self.editmenu.add_command(label="Copy", command=self.hello)
        self.menubar.add_cascade(label="Edit", menu=self.editmenu)
        self.root.config(menu=self.menubar)

        self.title_label = Label(self.root, text="Name Generator", bg='black', fg='white', font=("Impact", 24))
        self.generator_button = Button(self.root, text="Generate!", bg='black', fg='white',
                                       command=self.generate_name)
        self.name_label = Label(self.root, text="", padx=50, pady=10, bg='black', fg='red')
        self.title_label.grid(row=0, padx=75)
        self.name_label.grid(row=1)
        self.generator_button.grid(row=2)
        self.root.mainloop()

    def generate_name(self):
        self.name_label.config(text="")
        self.name_value = (random.choice(ELF_FNAME_PREFIX) + random.choice(ELF_FNAME_SUFFIX) + " " +
            random.choice(SURNAME_PART1) + random.choice(SURNAME_PART2))
        return Label(self.root, text=self.name_value, padx=50, bg='black',
                     fg='red', font=("Impact", 18)).grid(row=1)

    def hello(self):
        self.root.clipboard_clear()
        self.root.clipboard_append(self.name_value)


new_window = MainGUI()
